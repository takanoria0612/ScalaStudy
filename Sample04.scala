object Sample04{
    def main(args:Array[String]){
        println(3.14)
        println(1.2E2)
        println(1.2E-2D)
        println(4.5F)
        println(5.6D)
        println(7.8E3F)
        println(8.9E-3D)
    }
}
/*
アルファベットのEを用いて指数部を表現する小tができる。たとえば、1.2E2は1.2✖️10の２乗で、
1.2E-2は1.2✖️10の-２乗

浮動小数点数リテラルはデフォルトでDouble型
*/